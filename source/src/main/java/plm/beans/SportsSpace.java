package plm.beans;

public class SportsSpace {
    private Integer id;

    private Integer sportsManagerId;

    private String name;

    private Integer straightTrackNumber;

    private Integer bendTrackNumber;

    private Integer jumpingAreaNumber;

    private Integer highJumpingAreaNumber;

    private Integer shortThrowAreaNumber;

    private Integer longThrowAreaNumber;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getSportsManagerId() {
        return sportsManagerId;
    }

    public void setSportsManagerId(Integer sportsManagerId) {
        this.sportsManagerId = sportsManagerId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getStraightTrackNumber() {
        return straightTrackNumber;
    }

    public void setStraightTrackNumber(Integer straightTrackNumber) {
        this.straightTrackNumber = straightTrackNumber;
    }

    public Integer getBendTrackNumber() {
        return bendTrackNumber;
    }

    public void setBendTrackNumber(Integer bendTrackNumber) {
        this.bendTrackNumber = bendTrackNumber;
    }

    public Integer getJumpingAreaNumber() {
        return jumpingAreaNumber;
    }

    public void setJumpingAreaNumber(Integer jumpingAreaNumber) {
        this.jumpingAreaNumber = jumpingAreaNumber;
    }

    public Integer getHighJumpingAreaNumber() {
        return highJumpingAreaNumber;
    }

    public void setHighJumpingAreaNumber(Integer highJumpingAreaNumber) {
        this.highJumpingAreaNumber = highJumpingAreaNumber;
    }

    public Integer getShortThrowAreaNumber() {
        return shortThrowAreaNumber;
    }

    public void setShortThrowAreaNumber(Integer shortThrowAreaNumber) {
        this.shortThrowAreaNumber = shortThrowAreaNumber;
    }

    public Integer getLongThrowAreaNumber() {
        return longThrowAreaNumber;
    }

    public void setLongThrowAreaNumber(Integer longThrowAreaNumber) {
        this.longThrowAreaNumber = longThrowAreaNumber;
    }
}