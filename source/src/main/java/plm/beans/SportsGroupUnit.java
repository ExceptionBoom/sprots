package plm.beans;

public class SportsGroupUnit {
    private Integer id;

    private Integer sportsId;

    private Integer sportsGroupId;

    private Integer sportsCompetitionUnitId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getSportsId() {
        return sportsId;
    }

    public void setSportsId(Integer sportsId) {
        this.sportsId = sportsId;
    }

    public Integer getSportsGroupId() {
        return sportsGroupId;
    }

    public void setSportsGroupId(Integer sportsGroupId) {
        this.sportsGroupId = sportsGroupId;
    }

    public Integer getSportsCompetitionUnitId() {
        return sportsCompetitionUnitId;
    }

    public void setSportsCompetitionUnitId(Integer sportsCompetitionUnitId) {
        this.sportsCompetitionUnitId = sportsCompetitionUnitId;
    }
}